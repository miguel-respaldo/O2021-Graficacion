def suma(a,b):
    resultado = a + b
    return resultado

def resta(a,b):
    resultado = a - b
    return resultado

def multiplicacion(a,b):
    resultado = a * b
    return resultado

def division(a,b):
    resultado = a / b
    return resultado


print("La suma de 5 y 6 es ", suma(5,6))
print("La resta de 5 y 6 es ", resta(5,6))
print("La multiplicacion de 5 y 6 es ", multiplicacion(5,6))
print("La division de 5 y 6 es ", division(5,6))